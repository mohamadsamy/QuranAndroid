package com.atif.quranapp.Model;

public class TimeTable {
    String date,ramzan,sehri,aftari;

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getRamzan() {
        return ramzan;
    }

    public void setRamzan(String ramzan) {
        this.ramzan = ramzan;
    }

    public String getSehri() {
        return sehri;
    }

    public void setSehri(String sehri) {
        this.sehri = sehri;
    }

    public String getAftari() {
        return aftari;
    }

    public void setAftari(String aftari) {
        this.aftari = aftari;
    }
}
